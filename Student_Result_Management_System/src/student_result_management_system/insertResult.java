    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        String rollno = jTextField1.getText();
        String oop = jTextField2.getText();
        String os = jTextField3.getText();
        String dsa = jTextField4.getText();
        String dbms = jTextField5.getText();
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm", "root", "Ramahalder1@");
            
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from student where rollno ='"+rollno+"'");
            if(rs.next()){
                st.executeUpdate("insert into result(rollno,oop,os,dsa,dbms) values('"+rollno+"','"+oop+"','"+os+"','"+dsa+"','"+dbms+"')");
                JOptionPane.showMessageDialog(null, "Result is saved Successfully!");
                setVisible(false);
                new insertResult().setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(null, "Student is not registered!");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_saveActionPerformed

